  function get_packages() {


    let packageTypeId = $("input[name=menu_category_id]:checked").val();


    $.ajax({

      url: "function/food_package_process.php",
      type: "POST",
      dataType: 'html',
      data: "ajaxPackageTypeId=" + packageTypeId,
      success: function(response) {
        $('#package_id').html(response);
      },

      async: false



    });



    // $("selector").action();

  }


  function delete_package_details(id) {

    swal({
        title: "Are you sure?",
        text: "Your will not be able to recover this data",
        type: "warning",
        showCancelButton: true,
        confirmButtonClass: "btn-danger",
        confirmButtonText: "Yes, delete it!",
        closeOnConfirm: false
      },
      function() {

        $.ajax({

            url: "function/food_package_process.php",
            type: "POST",
            dataType: 'json',
            data: "food_package_delete_id=" + id,
            success: function(response) {

              swal("Deleted!", response.message, "success");
              $("#row_id_" + id).hide('slow');

            },

            async: false



            });

        

      });

    

  }
</script>


